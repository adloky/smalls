function getLems(x) {
    x = x.toLowerCase();
    var rs = [];
    if (lemExs2.has(x)) {
        rs.push(x);
        return rs;
    }
    
    rs.push(x);
    var l = lemExs.get(x);
    if (l) {
        rs.push(l);
        return rs;
    }
    
    var esRe = /(ss|zz|x|sh|ch|o|i)es$/;
    var sRe = /([^yuoaies]|[^yuoaie]e)s$/;
    var ingEdRe = /(ing|ed)$/;
    if (x.endsWith("s")) {
        if (esRe.test(x)) {
            x = x.replace(esRe, "$1");
            x = x.replace(/i$/, "y");
            rs.push(x);
        }
        else if (sRe.test(x)) {
            x = x.replace(sRe, "$1");
            rs.push(x);
        }
    }
    else if (ingEdRe.test(x)) {
        x = x.replace(ingEdRe, "");
        if (/[yuoaie]/.test(x) && /(bb|dd|ff|gg|mm|nn|pp|rr|tt)$/.test(x)) {
            x = x.substr(0, x.length - 1);
            rs.push(x);
        }
        else {
            rs.push(x);
            rs.push(x + "e");
        }
    }
    
    return rs;
}

